-- \c magsync;

-- 创建存储同步数据的表
CREATE TABLE IF NOT EXISTS t_magsync_config (
    id SERIAL PRIMARY KEY,
    key varchar(255) NOT NULL,
    value text NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);

-- 创建索引以提高查询性能
CREATE INDEX IF NOT EXISTS idx_config_updated_at 
ON t_magsync_config(updated_at DESC);


-- 创建触发器函数，自动更新 updated_at 时间戳
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = CURRENT_TIMESTAMP;
    RETURN NEW;
END;
$$ language 'plpgsql';

-- 创建触发器
DROP TRIGGER IF EXISTS update_config_updated_at ON t_magsync_config;
CREATE TRIGGER update_config_updated_at
    BEFORE UPDATE ON t_magsync_config
    FOR EACH ROW
    EXECUTE FUNCTION update_updated_at_column();

